    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $shop = new Shop();

        $shop->user_id = $request->user()->id;
        $shop->adress = $request->adress;
        $shop->company_code = $request->company_code;
        $shop->vat_code = $request->vat_code;
        $shop->phone_number = $request->phone_number;
        $shop->save();
        $data['shop_id'] = $shop->id;
        return redirect(route('categories.create', $data));
    }

